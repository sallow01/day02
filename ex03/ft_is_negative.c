#include <unistd.h>

void  ft_putchar(char c)

void ft_is_negative()
{
	int n;
	n = 0;
	while(n >= -21474383648 && n <= 2147483647)
	{
	     if(n < 0)
		 ft_putchar('N');
		 else if(n == 0 || n == '\0'|| n > 0)
		      ft_putchar('P');
	    break;
	}

}

	

